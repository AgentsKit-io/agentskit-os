#!/usr/bin/env node
// RFC-0007 enforcement. Snapshots the public TypeScript surface of every
// distribution=public package. Diffs against committed .agentskitos-api/
// snapshots; non-additive change without --update fails CI.
//
// Snapshot is intentionally NAMES + KINDS only — does not capture full
// type shapes. Catches renames, deletions, and additions; ignores
// purely structural type changes (e.g. literal-union reordering).
//
// Usage:
//   node scripts/check-public-api.mjs            # check (default)
//   node scripts/check-public-api.mjs --update   # rewrite snapshots
//
// Run after `pnpm build`. Note: the patterns below are simple regexes
// against `dist/index.d.ts` declaration files generated by tsup; they
// have no relation to child_process.

import { readFileSync, readdirSync, writeFileSync, mkdirSync, existsSync } from 'node:fs'
import { join, dirname } from 'node:path'

const ROOT = new URL('..', import.meta.url).pathname
const PACKAGES_DIR = join(ROOT, 'packages')
const SNAP_DIR = join(ROOT, '.agentskitos-api')
const UPDATE = process.argv.includes('--update')

const KIND_PATTERNS = [
  { kind: 'function', re: /^\s*declare\s+function\s+([A-Za-z_$][\w$]*)/gm },
  { kind: 'function', re: /^\s*export\s+declare\s+function\s+([A-Za-z_$][\w$]*)/gm },
  { kind: 'class', re: /^\s*declare\s+class\s+([A-Za-z_$][\w$]*)/gm },
  { kind: 'class', re: /^\s*export\s+declare\s+class\s+([A-Za-z_$][\w$]*)/gm },
  { kind: 'interface', re: /^\s*interface\s+([A-Za-z_$][\w$]*)/gm },
  { kind: 'type', re: /^\s*(?:declare\s+)?type\s+([A-Za-z_$][\w$]*)/gm },
  { kind: 'const', re: /^\s*declare\s+const\s+([A-Za-z_$][\w$]*)/gm },
  { kind: 'const', re: /^\s*export\s+declare\s+const\s+([A-Za-z_$][\w$]*)/gm },
]

const REEXPORT_RE = /^\s*export\s*(?:type\s+)?\{\s*([^}]+)\s*\}/gm

const parseDts = (raw) => {
  const out = new Map()
  for (const { kind, re } of KIND_PATTERNS) {
    re.lastIndex = 0
    let m
    while ((m = re.exec(raw)) !== null) {
      const ctx = raw.slice(Math.max(0, m.index - 200), m.index)
      const entry = { kind }
      if (/@deprecated/.test(ctx)) entry.deprecated = true
      out.set(m[1], entry)
    }
  }
  REEXPORT_RE.lastIndex = 0
  let m
  while ((m = REEXPORT_RE.exec(raw)) !== null) {
    const list = m[1].split(',').map((s) => s.trim()).filter(Boolean)
    for (const item of list) {
      const name = item.split(/\s+as\s+/i).pop()?.trim() ?? item
      if (!out.has(name)) out.set(name, { kind: 'reexport' })
    }
  }
  return Object.fromEntries([...out.entries()].sort(([a], [b]) => (a < b ? -1 : a > b ? 1 : 0)))
}

const publicPackages = []
for (const name of readdirSync(PACKAGES_DIR)) {
  const pkgPath = join(PACKAGES_DIR, name, 'package.json')
  if (!existsSync(pkgPath)) continue
  let pkg
  try { pkg = JSON.parse(readFileSync(pkgPath, 'utf8')) } catch { continue }
  if (pkg.agentskitos?.distribution !== 'public') continue
  publicPackages.push({ name: pkg.name, dir: join(PACKAGES_DIR, name) })
}

const violations = []
const additions = []

mkdirSync(SNAP_DIR, { recursive: true })

for (const { name, dir } of publicPackages) {
  const dtsPath = join(dir, 'dist', 'index.d.ts')
  if (!existsSync(dtsPath)) {
    violations.push(`${name}: dist/index.d.ts missing — run pnpm build first`)
    continue
  }
  const raw = readFileSync(dtsPath, 'utf8')
  const map = parseDts(raw)
  const snap = { exports: map }
  const snapPath = join(SNAP_DIR, `${name.replace('@agentskit/', '')}.json`)

  if (UPDATE || !existsSync(snapPath)) {
    mkdirSync(dirname(snapPath), { recursive: true })
    writeFileSync(snapPath, JSON.stringify(snap, null, 2) + '\n')
    if (!UPDATE) additions.push(`${name}: created initial snapshot at ${snapPath}`)
    continue
  }

  let prev
  try { prev = JSON.parse(readFileSync(snapPath, 'utf8')) } catch {
    violations.push(`${name}: snapshot at ${snapPath} is unreadable`)
    continue
  }

  const prevExports = prev.exports ?? {}
  const removed = []
  const renamed = []
  const added = []
  for (const k of Object.keys(prevExports)) {
    if (!(k in map)) removed.push(k)
    else if (prevExports[k].kind !== map[k].kind) renamed.push(`${k}: ${prevExports[k].kind} → ${map[k].kind}`)
  }
  for (const k of Object.keys(map)) {
    if (!(k in prevExports)) added.push(k)
  }

  if (removed.length > 0) violations.push(`${name}: removed exports — ${removed.join(', ')}`)
  if (renamed.length > 0) violations.push(`${name}: kind changes — ${renamed.join(', ')}`)
  if (added.length > 0) additions.push(`${name}: new exports — ${added.join(', ')}`)
}

if (additions.length > 0) {
  console.log('Additive public-API changes (informational):')
  for (const a of additions) console.log(`  + ${a}`)
  console.log('')
}

if (violations.length > 0) {
  console.error('RFC-0007 violation: non-additive public-API changes detected.')
  console.error('Each requires either:')
  console.error('  (a) revert / rename, or')
  console.error('  (b) major-bump changeset + run `node scripts/check-public-api.mjs --update`.\n')
  for (const v of violations) console.error(`  - ${v}`)
  console.error('\nSee docs/rfc/0007-contract-freeze.md')
  process.exit(1)
}

console.log(UPDATE ? 'Snapshots updated.' : 'RFC-0007 check passed: public API is stable or additive only.')
